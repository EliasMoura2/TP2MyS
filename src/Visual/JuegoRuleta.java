    private Jugador crearJugador(){
        Jugador unJugador =new Jugador(this.jugadores.size()+1);
        this.jugadores.add(unJugador);
        return unJugador;
    }
    
    private void crearJugadores(int cantidad){
        this.jugadores.clear();
        for(int i=0;i<cantidad;i++){
            this.crearJugador();
        }
    }
    
